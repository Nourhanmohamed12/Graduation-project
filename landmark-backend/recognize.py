def insert_metadata_to_db(image_path):
    metadata = get_landmark_metadata(image_path)
    if "error" in metadata:
        return {"error": "Metadata generation failed."}

    name = metadata.get("name", "").strip().lower()

    # Connect to DB
    conn = mysql.connector.connect(
        host='localhost',
        user='root',
        password='2468',
        database='tourism'
    )
    cursor = conn.cursor(dictionary=True)

    # Check for existing landmark with the same name (case-insensitive)
    cursor.execute("SELECT * FROM places WHERE LOWER(name) = %s", (name,))
    existing = cursor.fetchone()

    if existing:
        metadata["Landmark_ID"] = existing["Landmark_ID"]
        metadata["note"] = "Duplicate found. Returning existing entry."
        cursor.close()
        conn.close()
        return metadata

    # Proceed to create new ID if not found
    category = metadata.get("category", "UNK").upper()
    prefix_map = {
        "ANCIENT": "ANC",
        "MODERN": "MOD",
        "NATURE": "NAT",
        "RELIGIOUS": "REL",
        "TEMPLE": "TMP"
    }
    prefix = prefix_map.get(category, "UNK")

    cursor.execute("SELECT Landmark_ID FROM places WHERE Landmark_ID LIKE %s", (f"{prefix}_%",))
    existing_ids = [row["Landmark_ID"] for row in cursor.fetchall()]
    numbers = [int(re.search(rf"{prefix}_(\d+)", lid).group(1)) for lid in existing_ids if re.search(rf"{prefix}_(\d+)", lid)]
    next_id_number = max(numbers) + 1 if numbers else 1
    new_id = f"{prefix}_{next_id_number:02d}"

    cursor.execute("""
        INSERT INTO places (Landmark_ID, name, category, description, city, locationString, link_location, image_url, rating)
        VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)
    """, (
        new_id,
        metadata.get("name"),
        category,
        metadata.get("description"),
        metadata.get("city"),
        metadata.get("locationString"),
        metadata.get("link_location"),
        metadata.get("image_url"),
        metadata.get("rating")
    ))

    conn.commit()
    cursor.close()
    conn.close()

    metadata["Landmark_ID"] = new_id
    return metadata


def recognize_landmark(image_path, model, data_cat, confidence_threshold=80):
    img_height, img_width = 224, 224
    image = tf.keras.utils.load_img(image_path, target_size=(img_height, img_width))
    img_array = tf.keras.utils.img_to_array(image)
    img_batch = tf.expand_dims(img_array, 0)

    predictions = model.predict(img_batch, verbose=0)
    scores = tf.nn.softmax(predictions[0])
    confidence = np.max(scores) * 100
    predicted_label = data_cat[np.argmax(scores)]

    result = {}

    if confidence < confidence_threshold:
        metadata = insert_metadata_to_db(image_path)
        result = metadata
    else:
        db_metadata = get_metadata_from_db(predicted_label)

        if db_metadata:
            result = {
                "Landmark_ID": db_metadata.get("Landmark_ID", ""),
                "name": db_metadata.get("name", ""),
                "description": db_metadata.get("description", ""),
                "link_location": db_metadata.get("link_location", ""),
                "image_url": db_metadata.get("image_url", "")
            }
        else:
            result = {
                "Landmark_ID": predicted_label,
                "note": "Metadata not found in database."
            }

    return result


