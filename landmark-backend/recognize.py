def get_landmark_metadata(image_path):
    model = genai.GenerativeModel("gemini-1.5-flash")
    image = Image.open(image_path)

    prompt = """
You are an expert landmark identifier. 
Only output valid pure JSON object, without Markdown blocks or explanations.
Format:
{
  "name": "Landmark Name",
  "category": "Category (Ancient, Modern, Nature, Religious, Temple)",
  "description": "Short description",
  "city": "City Name",
  "locationString": "Formatted location",
  "link_location": "Google Maps or equivalent location URL",
  "rating": "Estimated user rating out of 5 (e.g., 4.7)"
}
"""

    response = model.generate_content([prompt, image])
    result_text = response.text.strip()

    # Clean up extra "Metadata:" and markdown backticks
    result_text = re.sub(r"^Metadata:\s*", "", result_text, flags=re.IGNORECASE)
    result_text = result_text.replace("```json", "").replace("```", "").strip()

    # Extract valid JSON
    try:
        metadata_raw = json.loads(result_text)
    except Exception as e:
        print("Error parsing metadata JSON:", e)
        return {"error": "Failed to parse metadata."}

        # Normalize keys with proper casing (keep 'locationString' etc.)
    def normalize_key(key):
        return key.strip().replace(" ", "").replace("Locationstring", "locationString")

    # Updated key cleanup
    metadata = {}
    for key, value in metadata_raw.items():
        fixed_key = normalize_key(key)

        # Clean and parse rating safely
        if fixed_key == "rating":
            try:
                if isinstance(value, str):
                    value = float(re.sub(r"[^\d.]", "", value))  # Remove non-numeric chars
                elif isinstance(value, (int, float)):
                    value = float(value)
                else:
                    value = 0.0
            except:
                value = 0.0

        metadata[fixed_key] = value



    # Now optionally add image_url if needed
    if "name" in metadata:
        image_url = get_duckduckgo_image_url(metadata["name"])
        if image_url:
            metadata["image_url"] = image_url

    return metadata


